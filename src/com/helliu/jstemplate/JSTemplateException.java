package com.helliu.jstemplate;

public class JSTemplateException extends Exception {

	private static final long serialVersionUID = -4034976638473532528L;

	public JSTemplateException() {
		super();
	}

	public JSTemplateException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public JSTemplateException(String message, Throwable cause) {
		super(message, cause);
	}

	public JSTemplateException(String message) {
		super(message);
	}

	public JSTemplateException(Throwable cause) {
		super(cause);
	}

	
}
